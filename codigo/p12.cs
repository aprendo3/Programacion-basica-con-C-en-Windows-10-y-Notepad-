using System;
class Program
{
    static void Main()
    {
        int a = 5;
        int b = 2;

        int c = a + b;
        Console.WriteLine("{0} + {1} = {2}", a, b ,c);

        c = a - b;
        Console.WriteLine("{0} - {1} = {2}", a, b ,c);

        c = a * b;
        Console.WriteLine("{0} * {1} = {2}", a, b ,c);

        c = a / b;
        Console.WriteLine("{0} / {1} = {2}", a, b ,c);

        c = a % b;
        Console.WriteLine("{0} % {1} = {2}", a, b ,c);
    }
}
